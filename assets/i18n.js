function normalizeLanguagePreference(value) {
  const normalized = String(value || '').toLowerCase().slice(0, 2);
  return SUPPORTED_LANGUAGES.has(normalized) ? normalized : 'en';
}

function getLanguagePreference() {
  return normalizeLanguagePreference(STATE.language || 'en');
}

function getNextLanguagePreference() {
  return getLanguagePreference() === 'nl' ? 'en' : 'nl';
}

function getLanguageToggleText() {
  return getLanguagePreference() === 'nl' ? 'EN' : 'NL';
}

function getLanguageToggleTitle() {
  return getLanguagePreference() === 'nl' ? 'Switch to English' : 'Wissel naar Nederlands';
}

function loadLanguagePreference() {
  try {
    STATE.language = normalizeLanguagePreference(localStorage.getItem(LANGUAGE_STORAGE_KEY));
  } catch {
    STATE.language = 'en';
  }
  applyLanguagePreference();
}

function setLanguagePreference(value) {
  STATE.language = normalizeLanguagePreference(value);
  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, STATE.language);
  } catch {
    // Local storage may be unavailable in restricted browser contexts.
  }
  applyLanguagePreference();
}

function applyLanguagePreference() {
  if (typeof document === 'undefined' || !document.documentElement) return;
  document.documentElement.lang = getLanguagePreference();
  document.documentElement.dataset.language = getLanguagePreference();
}

function translateCopy(value) {
  if (value == null) return '';
  const raw = String(value);
  if (getLanguagePreference() === 'en') return raw;

  const leading = raw.match(/^\s*/)[0];
  const trailing = raw.match(/\s*$/)[0];
  let text = raw.trim();
  if (!text) return raw;

  for (const [pattern, replacement] of NL_DYNAMIC_PATTERNS) {
    if (pattern.test(text)) {
      return leading + text.replace(pattern, replacement) + trailing;
    }
  }

  if (Object.prototype.hasOwnProperty.call(NL_COPY, text)) {
    return leading + NL_COPY[text] + trailing;
  }

  const replacements = Object.entries(NL_COPY).sort((a, b) => b[0].length - a[0].length);
  for (const [english, dutch] of replacements) {
    if (english.length < 4 || english === dutch || !text.includes(english)) continue;
    text = replaceUiCopyFragment(text, english, dutch);
  }

  return leading + text + trailing;
}

function replaceUiCopyFragment(text, english, dutch) {
  const startsWord = /^[A-Za-z0-9]/.test(english);
  const endsWord = /[A-Za-z0-9]$/.test(english);
  let result = '';
  let index = 0;

  while (index < text.length) {
    const foundAt = text.indexOf(english, index);
    if (foundAt === -1) {
      result += text.slice(index);
      break;
    }

    const before = foundAt > 0 ? text[foundAt - 1] : '';
    const after = text[foundAt + english.length] || '';
    const isEmbeddedInProductToken =
      (startsWord && /[A-Za-z0-9]/.test(before)) ||
      (endsWord && /[A-Za-z0-9]/.test(after));

    result += text.slice(index, foundAt);
    result += isEmbeddedInProductToken ? english : dutch;
    index = foundAt + english.length;
  }

  return result;
}

function translateRenderedApp(root) {
  applyLanguagePreference();
  if (!root || getLanguagePreference() === 'en' || typeof document === 'undefined') return;
  if (typeof document.createTreeWalker !== 'function' || typeof NodeFilter === 'undefined') return;

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (parent.closest('[data-i18n-skip]')) return NodeFilter.FILTER_REJECT;
      if (['SCRIPT', 'STYLE', 'TEXTAREA', 'CODE'].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      return /\S/.test(node.nodeValue || '') ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(node => {
    node.nodeValue = translateCopy(node.nodeValue);
  });

  root.querySelectorAll(TRANSLATABLE_ATTRIBUTES.map(attr => `[${attr}]`).join(',')).forEach(element => {
    if (element.closest('[data-i18n-skip]')) return;
    TRANSLATABLE_ATTRIBUTES.forEach(attribute => {
      if (!element.hasAttribute(attribute)) return;
      element.setAttribute(attribute, translateCopy(element.getAttribute(attribute)));
    });
  });
}

